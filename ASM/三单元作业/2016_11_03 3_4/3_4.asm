DATA SEGMENT
	SRT	DB 	100
		DB	?
		DB 	100	DUP(?)
	TS 	DB 	'Please input a character!$'
	HH	DB 	0DH,0AH,'$'
	WA	DB	'Input is not valid$'
	YES	DB	'Y$'
	NO 	DB 	'N$'
DATA ENDS
STACK SEGMENT STACK
	DB 100 DUP(?)
STACK ENDS

CODE SEGMENT
	ASSUME DS:DATA,CS:CODE,SS:STACK
INPUT MACRO SR
	LEA DX, SR
	MOV AH, 0AH
	INT 21H
	ENDM
OUTPUT MACRO SR
	LEA DX, SR
	MOV AH, 09H
	INT 21H
	ENDM
MAIN PROC FAR
	MOV AX, DATA
	MOV DS, AX
	NEXT:
		OUTPUT TS
		OUTPUT HH
		INPUT SRT
		CALL CHECK
		CMP AX ,0
		JNZ ED
		OUTPUT WA
		OUTPUT HH
		JMP NEXT
	ED:
	CALL FINISH
	RET
MAIN ENDP
FINISH PROC
	MOV AH, 0
	INT 16H
	MOV AH,4CH
	INT 21H
	RET
FINISH ENDP
CHECK PROC 
	MOV CL, SRT+1
	MOV AX, 0
	CMP CL, 1
	JNZ  ISVALID
	MOV DL, SRT+2
	CMP DL, 'Y'
	JZ ISYES
	CMP DL, 'y'
	JZ ISYES
	CMP DL, 'N'
	JZ ISNO
	CMP DL, 'n'
	JZ ISNO
	ISVALID:
		MOV AX,0
		RET	
	ISNO :
		OUTPUT NO
		OUTPUT HH
		MOV AX, 2
		RET
	ISYES:
		OUTPUT YES
		OUTPUT HH
		MOV AX, 1 
		RET
	RET
CHECK ENDP
CODE ENDS
	END MAIN
