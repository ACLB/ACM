DATA	SEGMENT 
	BUFFER	DW	76,45,73,42,83,67
	MIN		DW 	0
	SUM		DW 	0
	MOOD	DW 	0AH
	TSM		DB	'The min num is : $'
	HH		DB	0DH,0AH,'$'
	TSS		DB	'The sum is : $'
DATA	ENDS
STACK	SEGMENT	STACK
	DB	100	DUP(?)
STACK 	ENDS
CODE	SEGMENT
	ASSUME DS:DATA,CS:CODE,SS:STACK
OUTPUTSTR MACRO SR
	LEA DX, SR
	MOV AH, 09H
	INT 21H
	ENDM
OUTPUTCHAR MACRO CHA
	MOV DL,CHA
	ADD DL,'0'
	MOV AH, 02H
	INT 21H
	ENDM
OUTPUTNUM	MACRO	NUM
	LOCAL NEXT
	LOCAL NEXTOUT
	MOV AX,NUM
	CWD
	MOV CX, 0
	NEXT:
		DIV	MOOD
		PUSH DX
		INC CX
		CMP AX,0
		JZ NEXTOUT
		CWD
		JMP NEXT
	NEXTOUT:
		POP DX
		OUTPUTCHAR 	DL
		DEC CX
		JNZ NEXTOUT
	ENDM
MAIN	PROC
	MOV AX,DATA
	MOV DS,AX
	CALL GETMINSUM
	OUTPUTSTR TSM
	OUTPUTNUM MIN
	OUTPUTSTR HH
	OUTPUTSTR TSS
	OUTPUTNUM SUM
	OUTPUTSTR HH
	CALL FINISH
	RET 
MAIN	ENDP
FINISH PROC
	MOV AH,00H
	INT 16H
	MOV AH,4CH
	INT 21H
	RET
FINISH ENDP
GETMINSUM PROC
	LEA SI, BUFFER
	MOV CX, 06H
	NEXTT:
		MOV DX, [SI]
		MOV AX, SUM
		ADD AX, DX
		MOV SUM,AX
		CMP DX,MIN
		JL ISMIN
			MOV MIN,DX
		ISMIN:
			INC SI
			INC SI
			DEC CX
		JNZ NEXTT
	JNZ NEXTT
	RET
GETMINSUM ENDP
CODE 	ENDS
	END		MAIN